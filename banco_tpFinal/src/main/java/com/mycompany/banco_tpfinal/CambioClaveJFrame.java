    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        try{
            boolean bien = nuevaClave();
            if (bien) {
                this.setVisible(false);
                Operaciones op = new Operaciones(registro, login);    // Necesito "registro" y "login" para actualizar los datos.
                op.setVisible(true);
                op.repaint();
                op.validate();
            }

        } catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e+"\n\nTienes que iniciar sesión antes", "ERROR", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAceptarActionPerformed

