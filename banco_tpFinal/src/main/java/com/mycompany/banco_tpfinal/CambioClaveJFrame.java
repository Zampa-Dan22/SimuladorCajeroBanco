    /**
     * 
     * <p>"nuevaClave()" : Funcion que realiza el cambio de contraseña.</p>
     * @return Descripcion: Retorna un valor boolean que indicará si se realizó 
     * el cambio de contraseña o si no se realizó. 
     */
    public boolean nuevaClave(){
        Operaciones op = new Operaciones(registro, login);
        
        if(registro.listaClientes.get(op.codigo).getContra().equals(txtContraVieja.getText())){
            if (!txtContraNueva.getText().isEmpty()){    
                if (txtContraNueva.getText().equals(txtRepetirContra.getText())){
                    registro.listaClientes.get(op.codigo).setContra(txtContraNueva.getText());
                    JOptionPane.showMessageDialog(rootPane, "cambio de contraseña realizado con éxito");
                    JOptionPane.showMessageDialog(rootPane, "Nueva contraseña: "+registro.listaClientes.get(op.codigo).contra);
                    return true;
                } else {
                    JOptionPane.showMessageDialog(rootPane, "La nueva contraseña no es igual a la repetida,\n no se realizó el cambio de contraseña", "ERROR", JOptionPane.ERROR_MESSAGE);
                    txtRepetirContra.setText(null);
                    txtRepetirContra.requestFocus();
                    return false;
                }
            } else {
                 JOptionPane.showMessageDialog(rootPane, "No puede dejar el campo 'Nueva Contraseña' vacío,\n ingrese una nueva contraseña para realizar el cambio de contraseña", "ERROR", JOptionPane.ERROR_MESSAGE);
                 txtContraNueva.setText(null);
                 txtContraNueva.requestFocus();
                 return false;
                }
        } else {
            JOptionPane.showMessageDialog(rootPane, "La contraseña actual no es la correcta,\n no se realizó el cambio de clave", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtContraVieja.setText(null);
            txtContraVieja.requestFocus();
            return false;
        } 
    
    }
    
